#include <iostream>
#include <string>

using namespace std;

int main(){
	int x = 9;		// Esto es un entero
	double y = 10.2;	// Esto es un decimal 
	string z = "Hello Cadena";	// Esto es un string o cadena
	bool boleano = true;	// Esto es un valor booleano

	cout << "Esto es un entero: " << x << endl;
	cout << "Esto es un decimal: "<< y << endl;
	cout << "Esto es una cadena: "<< z << endl;
	cout << "Esto es un booleano: "<< boleano << endl;

	return 0;
}
